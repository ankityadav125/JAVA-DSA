package Numericals_100_Question;

public class Ques01_Odd_Number1_100 {
    public static void main(String[] args) {
        System.out.println("Odd number is : ");
        for(int i=1;i<=100;i++){
            if (i % 2 != 0) {
                System.out.println(i);
            }
        }
    }
}
